class PrintHello {
    public void HelloWorld()
    {
        System.out.println("Hello World");
    }

    public void HelloStr(String w)
    {
        System.out.println("Hello " + w);
    }

    public void HelloForLoop()
    {
        for(int i = 0; i < 10; i++)
        {
            System.out.println(i + " : HelloWorld");
        }
    }
}
